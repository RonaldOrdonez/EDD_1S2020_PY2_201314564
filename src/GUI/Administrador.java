    public void leerJsonUsuarios(String archivo) throws org.json.simple.parser.ParseException, InterruptedException {
        
        txtMasivaUsuarios.setText("");
        String contenido = "";
        JSONParser parser = new JSONParser();
        try {
            Object obj = parser.parse(new FileReader(archivo));
            
            JSONObject jsonObject = (JSONObject)obj;
            //System.out.println("JSON LEIDO :" + jsonObject);
            
            JSONArray array = (JSONArray) jsonObject.get("Usuarios");
            //System.out.println("");
            for(int i = 0; i < array.size(); i++) 
            {
               JSONObject jsonObject1 =  (JSONObject)array.get(i);
                //System.out.println("DATOS DEL USUARIO: "+i);
                String carnet = jsonObject1.get("Carnet").toString();
                String nombre = jsonObject1.get("Nombre").toString();
                String apellido = jsonObject1.get("Apellido").toString();
                String carrera = jsonObject1.get("Carrera").toString();
                String contrasenia = (String) jsonObject1.get("Password");               
                
               if(tablahash.insertarUsuario(Integer.parseInt(carnet), nombre, apellido, carrera, contrasenia))               
               {
                    contenido += carnet+"-";
                    contenido += nombre+"-";
                    contenido += apellido+"-";
                    contenido += carrera+"-";                    
                    contenido += contrasenia+"\n";                 
                   
               }

                                    
            }
            txtMasivaUsuarios.setText(contenido);
           
        } catch (FileNotFoundException e) {
            //manejo de error
        } catch (IOException e) {
            //manejo de error
        }

    }

