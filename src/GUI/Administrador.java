    private void jmCargaMasivaUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmCargaMasivaUsuariosActionPerformed

        //tipo de archivo a abrir
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos JSON", "json");

        // muestra el cuadro de diálogo de archivos, para que el usuario pueda elegir el archivo a abrir
        JFileChooser selectorArchivos = new JFileChooser();
        selectorArchivos.setFileFilter(filtro);
        selectorArchivos.showOpenDialog(this);
        File archivo = selectorArchivos.getSelectedFile(); // obtiene el archivo seleccionado

        if (archivo != null) {
            try {
                leerJsonUsuarios(archivo.getAbsolutePath());
            } catch (org.json.simple.parser.ParseException ex) {
                JOptionPane.showMessageDialog(null, ex + "" + "\nNo se ha encontrado el archivo", "ADVERTENCIA!!!", JOptionPane.WARNING_MESSAGE);
            } catch (InterruptedException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jmCargaMasivaUsuariosActionPerformed

