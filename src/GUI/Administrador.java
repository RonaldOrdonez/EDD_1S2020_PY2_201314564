    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int carnet;
        String nombre;
        String apellido;
        String carrera;
        String contrasenia;

        if ("".equals(txtnombre.getText()) || "".equals(txtapellido.getText()) || "".equals(txtcarrera.getText()) || "".equals(txtcarnet.getText()) || "".equals(txtcontrasenia.getText())) {
            JOptionPane.showMessageDialog(null, "NO PUEDE DEJAR CAMPOS DE INFORMACION VACIOS");
        } else {
            carnet = Integer.parseInt(txtcarnet.getText());
            nombre = txtnombre.getText();
            apellido = txtapellido.getText();
            carrera = txtcarrera.getText();
            contrasenia = txtcontrasenia.getText();

            if (tablahash.insertarUsuario(carnet, nombre, apellido, carrera, contrasenia)) {
                JOptionPane.showMessageDialog(null, "USUARIO AGREGADO EXITOSAMENTE");
                limpiarCampos();
                btnGuardar.setEnabled(false);
                btnBuscar.setEnabled(true);
                deshabilitarCampos();
            } else {
                int seleccion = JOptionPane.showOptionDialog(
                        null, "Usuario ya existe. ¿Intentar de nuevo?", "Opciones", JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null, new Object[]{"SI", "NO"}, "opcion 1");
                if (seleccion == 1) {
                    limpiarCampos();
                    btnGuardar.setEnabled(false);
                    deshabilitarCampos();
                    btnBuscar.setEnabled(true);

                }
            }
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int carnet = Integer.parseInt(txtcarnet.getText());
        Usuario usuario;
        try {
            usuario = tablahash.buscar(carnet);
            if (usuario == null) {
                JOptionPane.showMessageDialog(null, "USUARIO NO EXISTE");
            } else {
                habilitarCampos();
                txtcarnet.enable(false);
                btnGuardar.setEnabled(false);
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);

                txtnombre.setText(usuario.nombre);
                txtapellido.setText(usuario.apellido);
                txtcarrera.setText(usuario.carrera);
                txtcontrasenia.setText(usuario.contrasenia);
            }
        } catch (Exception ex) {
            Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

